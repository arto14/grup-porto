        let currentPersonId = 1;

        // Initialize the application
        function init() {
            renderPersonGrid();
            showPerson(1);
        }

        // Render person selection grid
        function renderPersonGrid() {
            const grid = document.getElementById('personGrid');
            grid.innerHTML = people.map(person => `
                <div class="person-card ${person.id === currentPersonId ? 'active' : ''}" 
                     onclick="showPerson(${person.id})">
                    <div class="person-avatar">
                        <img src="${person.photo}" alt="${person.name}" onerror="this.style.display='none'; this.parentElement.innerHTML='${person.avatar}';">
                    </div>
                    <p class="person-name">${person.name}</p>
                </div>
            `).join('');
        }

        // Show selected person's profile
        function showPerson(personId) {
            const person = people.find(p => p.id === personId);
            if (!person) return;

            currentPersonId = personId;
            
            // Update active state in grid
            document.querySelectorAll('.person-card').forEach(card => {
                card.classList.remove('active');
            });
            document.querySelector(`[onclick="showPerson(${personId})"]`).classList.add('active');

            // Update profile content with animation
            const mainContent = document.getElementById('mainContent');
            mainContent.style.opacity = '0.5';
            
            setTimeout(() => {
                // Update profile header
                const profileAvatar = document.getElementById('profileAvatar');
                profileAvatar.innerHTML = `<img src="${person.photo}" alt="${person.name}" onerror="this.style.display='none'; this.parentElement.innerHTML='${person.avatar}';">`;
                document.getElementById('profileName').textContent = person.name;
                document.getElementById('profileRole').textContent = person.role;
                
                // Update about section
                document.getElementById('aboutContent').textContent = person.about;
                
                // Update skills section
                const skillsGrid = document.getElementById('skillsGrid');
                skillsGrid.innerHTML = person.skills.map(skill => `
                    <div class="skill-item">
                        <div class="skill-name">${skill.name}</div>
                        <div class="skill-level">
                            <div class="skill-progress" style="width: 0%"></div>
                        </div>
                        <div class="skill-percentage">${skill.level}%</div>
                    </div>
                `).join('');

                // Update social media section
                const socialMediaHtml = Object.entries(person.socialMedia).map(([platform, url]) => `
                    <a href="${url}" target="_blank" class="social-link">
                        <i class="fab fa-${platform}"></i>
                    </a>
                `).join('');
                document.getElementById('socialMedia').innerHTML = socialMediaHtml;
                
                // Update story section
                document.getElementById('storyContent').textContent = person.story;
                
                // Animate skill bars
                setTimeout(() => {
                    const skillBars = skillsGrid.querySelectorAll('.skill-progress');
                    skillBars.forEach((bar, index) => {
                        setTimeout(() => {
                            bar.style.width = person.skills[index].level + '%';
                        }, index * 100);
                    });
                }, 100);
                
                // Fade in content
                mainContent.style.opacity = '1';
            }, 300);
        }

        // Smooth scroll to main content when person is selected
        function smoothScrollToContent() {
            document.getElementById('mainContent').scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }

        // Add click event with smooth scroll for mobile
        document.addEventListener('DOMContentLoaded', function() {
            init();
            
            // Add smooth scroll for mobile devices
            if (window.innerWidth <= 768) {
                const originalShowPerson = window.showPerson;
                window.showPerson = function(personId) {
                    originalShowPerson(personId);
                    setTimeout(smoothScrollToContent, 400);
                };
            }
        });

        // Add parallax effect on scroll
        window.addEventListener('scroll', function() {
            const scrolled = window.pageYOffset;
            const parallax = document.querySelector('.animated-bg');
            const speed = scrolled * 0.5;
            parallax.style.transform = `translateY(${speed}px)`;
        });

        // Add window resize handler
        window.addEventListener('resize', function() {
            // Re-render grid if needed
            if (window.innerWidth !== window.lastWidth) {
                window.lastWidth = window.innerWidth;
                renderPersonGrid();
            }
        });

